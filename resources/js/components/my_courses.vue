export default {
  name: "my-courses",
  data() {
    return {
      id: "",
      courses: [],
      form: {
        semester: "",
        level: "",
      },
      errors: {},
    };
  },
  created() {
    this.login();
    this.getCourses();
  },
  methods: {
    login() {
      if (!User.loggedIn()) {
        this.$router.push({
          name: "login",
        });
      } else {
        this.id = User.id();
      }
    },
    getCourses() {
      axios
        .get("/api/v1/my-courses/" + this.id)
        .then(({ data }) => (this.courses = data))
        .catch();
    },
    update() {
      axios
        .patch("/api/v1/update-my-course/" + this.id, this.form)
        .then((response) => {
          this.$router.push({
            name: "dashboard",
          });
          Toast.fire({
            icon: "success",
            title: "Successfully Done",
          });
        })
        .catch((error) => (this.errors = error.response.data.errors));
    },
  },
};
</script>
